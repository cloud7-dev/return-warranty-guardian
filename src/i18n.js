export function normalizeLanguage(value) {
  return translations[value] ? value : DEFAULT_LANGUAGE;
}

export function translate(language, key, values = {}) {
  const template = translations[normalizeLanguage(language)][key] ?? translations[DEFAULT_LANGUAGE][key] ?? key;
  return Object.entries(values).reduce((text, [name, value]) => text.replaceAll(`{${name}}`, String(value)), template);
}

export function languageMeta(language) {
  return languages.find((item) => item.code === normalizeLanguage(language)) || languages[0];
}
